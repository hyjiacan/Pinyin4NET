namespace hyjiacan.py4n.format
{
    /// <summary>
    /// V(ü)字符格式
    /// </summary>
    public enum VCharFormat
    {
        /// <summary>
        /// 将 ü 输出为 u:
        /// </summary>
        WITH_U_AND_COLON,
        /// <summary>
        /// 将 ü 输出为 v
        /// </summary>
        WITH_V,
        /// <summary>
        /// 将 ü 输出为ü
        /// </summary>
        WITH_U_UNICODE
    }
}
